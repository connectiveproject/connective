export function getDialogOptions(currentOptions) {
  // :object currentOptions: the current avatar options has. e.g., { topType: "Hat", clotheColor: "Red", ... }
  // used to get the default dialog value
  const dialogOptions = []
  for (const [category, choices] of Object.entries(avatarCategories)) {
    dialogOptions.push({
      name: category,
      label: i18n.t(`avatar.${category}`),
      type: "select",
      rule: "required",
      value: currentOptions[category],
      choices,
    })
  }
  return dialogOptions
}

